@contextmanager
def _pushd(path: Path):
    import os

    previous = Path.cwd()
    os.chdir(path)
    try:
        yield
    finally:
        os.chdir(previous)


def _metadata_comment_lines(metadata: dict[str, Any] | None) -> list[str]:
    if not metadata:
        return []
    lines = []
    for key, value in metadata.items():
        if value is not None:
            lines.append(f"// {key}: {value}")
    return lines


def _is_generated_project_file(path: Path) -> bool:
    """
    判断文件是否属于本项目导出的动力学生成物。

    这里只清理 output 根目录下由本导出器生成的 `.h/.cpp/.inc`，
    避免误删用户手写代码或测试支架。
    """
    if path.suffix not in {".h", ".cpp", ".inc"}:
        return False
    if not path.is_file():
        return False
    try:
        first_lines = path.read_text(encoding="utf-8").splitlines()[:3]
    except UnicodeDecodeError:
        return False
    joined = "\n".join(first_lines)
    return "Auto-generated by dynamic_regress/run_pipeline.py." in joined


def _clear_stale_generated_project_files(
    output_dir: Path,
    *,
    keep_paths: set[Path],
) -> None:
    removed = []
    for candidate in output_dir.iterdir():
        if candidate in keep_paths or candidate.name == ".codegen_tmp":
            continue
        if _is_generated_project_file(candidate):
            candidate.unlink()
            removed.append(candidate.name)
    if removed:
        print(
            "Cleared stale generated dynamics file(s): "
            + ", ".join(sorted(removed))
        )


