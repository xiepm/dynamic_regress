def _format_double(value: float) -> str:
    return f"{float(value):.17g}"


def _format_c_array(values: list[float] | np.ndarray, *, indent: str = "\t") -> str:
    numbers = [_format_double(value) for value in values]
    if not numbers:
        return "{}"
    rows = []
    for start in range(0, len(numbers), 6):
        rows.append(indent + ", ".join(numbers[start:start + 6]))
    return "{\n" + ",\n".join(rows) + "\n}"


def _format_c_matrix(values: list[list[float]] | np.ndarray) -> str:
    matrix = np.asarray(values, dtype=float)
    rows = []
    for row in matrix:
        rows.append("\t{" + ", ".join(_format_double(value) for value in row) + "}")
    return "{\n" + ",\n".join(rows) + "\n}"


def _feature_expression(feature_name: str, num_joints: int) -> str:
    one_joint_patterns = [
        (r"^q_(\d+)$", "q[{idx}]"),
        (r"^dq_(\d+)$", "dq[{idx}]"),
        (r"^ddq_(\d+)$", "ddq[{idx}]"),
        (r"^abs_dq_(\d+)$", "fabs(dq[{idx}])"),
        (r"^abs_ddq_(\d+)$", "fabs(ddq[{idx}])"),
        (r"^q_dq_prod_(\d+)$", "q[{idx}] * dq[{idx}]"),
        (r"^sin_q_(\d+)$", "sin(q[{idx}])"),
        (r"^cos_q_(\d+)$", "cos(q[{idx}])"),
    ]
    for pattern, expression_template in one_joint_patterns:
        match = re.match(pattern, feature_name)
        if match:
            joint_index = int(match.group(1)) - 1
            if joint_index < 0 or joint_index >= num_joints:
                raise ValueError(f"Feature {feature_name!r} references joint outside 1..{num_joints}.")
            return expression_template.format(idx=joint_index)

    match = re.match(r"^dq_pair_(\d+)_(\d+)$", feature_name)
    if match:
        first = int(match.group(1)) - 1
        second = int(match.group(2)) - 1
        if first < 0 or second < 0 or first >= num_joints or second >= num_joints:
            raise ValueError(f"Feature {feature_name!r} references joint outside 1..{num_joints}.")
        return f"dq[{first}] * dq[{second}]"

    if feature_name == "motion_magnitude":
        terms = " + ".join(f"dq[{joint_idx}] * dq[{joint_idx}]" for joint_idx in range(num_joints))
        return f"sqrt({terms})"

    raise ValueError(f"Unsupported MLP feature {feature_name!r}; cannot export C++ code.")


def _make_mlp_core_code(mlp_state: dict[str, Any], num_joints: int) -> str:
    feature_cols = list(mlp_state["feature_cols"])
    x_mean = np.asarray(mlp_state["x_mean"], dtype=float)
    x_scale = np.asarray(mlp_state["x_scale"], dtype=float)
    y_mean = np.asarray(mlp_state["y_mean"], dtype=float)
    y_scale = np.asarray(mlp_state["y_scale"], dtype=float)
    coefs = [np.asarray(coef, dtype=float) for coef in mlp_state["coefs"]]
    intercepts = [np.asarray(bias, dtype=float) for bias in mlp_state["intercepts"]]

    if mlp_state.get("activation") != "relu":
        raise ValueError("Only ReLU MLP compensation can be exported.")
    if len(coefs) != len(intercepts):
        raise ValueError("MLP coefficient/intercept layer count mismatch.")
    if len(feature_cols) != x_mean.size or len(feature_cols) != x_scale.size:
        raise ValueError("MLP feature column count does not match input scaler size.")
    if y_mean.size != num_joints or y_scale.size != num_joints:
        raise ValueError("MLP output scaler size does not match robot joint count.")
    if not coefs or coefs[-1].shape[1] != num_joints:
        raise ValueError("MLP output dimension does not match robot joint count.")
    if coefs[0].shape[0] != len(feature_cols):
        raise ValueError("MLP first layer input dimension does not match feature count.")

    layer_dims = [coefs[0].shape[0]]
    for layer_idx, coef in enumerate(coefs):
        if coef.shape[0] != layer_dims[-1]:
            raise ValueError(f"MLP layer {layer_idx} input dimension mismatch.")
        if intercepts[layer_idx].shape[0] != coef.shape[1]:
            raise ValueError(f"MLP layer {layer_idx} bias dimension mismatch.")
        layer_dims.append(coef.shape[1])

    lines = [
        f"static const int kMlpInputDim = {len(feature_cols)};",
        f"static const int kMlpOutputDim = {num_joints};",
    ]
    for layer_idx, dim in enumerate(layer_dims[1:]):
        lines.append(f"static const int kMlpLayer{layer_idx}Dim = {dim};")
    lines.extend(
        [
            "",
            f"static const double kMlpXMean[kMlpInputDim] = {_format_c_array(x_mean)};",
            f"static const double kMlpXScale[kMlpInputDim] = {_format_c_array(x_scale)};",
            f"static const double kMlpYMean[kMlpOutputDim] = {_format_c_array(y_mean)};",
            f"static const double kMlpYScale[kMlpOutputDim] = {_format_c_array(y_scale)};",
        ]
    )
    for layer_idx, (coef, bias) in enumerate(zip(coefs, intercepts)):
        input_dim = "kMlpInputDim" if layer_idx == 0 else f"kMlpLayer{layer_idx - 1}Dim"
        output_dim = "kMlpOutputDim" if layer_idx == len(coefs) - 1 else f"kMlpLayer{layer_idx}Dim"
        lines.extend(
            [
                "",
                f"static const double kMlpW{layer_idx}[{input_dim}][{output_dim}] = {_format_c_matrix(coef)};",
                f"static const double kMlpB{layer_idx}[{output_dim}] = {_format_c_array(bias)};",
            ]
        )

    lines.extend(
        [
            "",
            "static double relu(double value)",
            "{",
            "\treturn value > 0.0 ? value : 0.0;",
            "}",
            "",
            "static void calculateMlpResidualCompensationCore(",
            "\tconst double* q, const double* dq, const double* ddq, double* residual)",
            "{",
            "\tdouble features[kMlpInputDim];",
        ]
    )
    for feature_idx, feature_name in enumerate(feature_cols):
        lines.append(f"\tfeatures[{feature_idx}] = {_feature_expression(feature_name, num_joints)};")

    lines.extend(
        [
            "\tdouble h0[kMlpLayer0Dim];",
            "\tfor (int outIdx = 0; outIdx < kMlpLayer0Dim; ++outIdx)",
            "\t{",
            "\t\tdouble value = kMlpB0[outIdx];",
            "\t\tfor (int inIdx = 0; inIdx < kMlpInputDim; ++inIdx)",
            "\t\t{",
            "\t\t\tconst double normalized = (features[inIdx] - kMlpXMean[inIdx]) / kMlpXScale[inIdx];",
            "\t\t\tvalue += normalized * kMlpW0[inIdx][outIdx];",
            "\t\t}",
            "\t\th0[outIdx] = relu(value);",
            "\t}",
        ]
    )

    for layer_idx in range(1, len(coefs) - 1):
        prev = f"h{layer_idx - 1}"
        current = f"h{layer_idx}"
        prev_dim = f"kMlpLayer{layer_idx - 1}Dim"
        current_dim = f"kMlpLayer{layer_idx}Dim"
        lines.extend(
            [
                f"\tdouble {current}[{current_dim}];",
                f"\tfor (int outIdx = 0; outIdx < {current_dim}; ++outIdx)",
                "\t{",
                f"\t\tdouble value = kMlpB{layer_idx}[outIdx];",
                f"\t\tfor (int inIdx = 0; inIdx < {prev_dim}; ++inIdx)",
                "\t\t{",
                f"\t\t\tvalue += {prev}[inIdx] * kMlpW{layer_idx}[inIdx][outIdx];",
                "\t\t}",
                f"\t\t{current}[outIdx] = relu(value);",
                "\t}",
            ]
        )

    output_layer_idx = len(coefs) - 1
    prev = f"h{output_layer_idx - 1}"
    prev_dim = f"kMlpLayer{output_layer_idx - 1}Dim"
    lines.extend(
        [
            "\tfor (int outIdx = 0; outIdx < kMlpOutputDim; ++outIdx)",
            "\t{",
            f"\t\tdouble value = kMlpB{output_layer_idx}[outIdx];",
            f"\t\tfor (int inIdx = 0; inIdx < {prev_dim}; ++inIdx)",
            "\t\t{",
            f"\t\t\tvalue += {prev}[inIdx] * kMlpW{output_layer_idx}[inIdx][outIdx];",
            "\t\t}",
            "\t\tresidual[outIdx] = value * kMlpYScale[outIdx] + kMlpYMean[outIdx];",
            "\t}",
            "}",
            "",
        ]
    )
    return "\n".join(lines)


def export_identified_inverse_dynamics_cpp(
    *,
    robot_model,
    result: dict,
    urdf_path: Path,
    output_dir: Path,
    result_stem: str,
    mlp_compensator=None,
    generation_metadata: dict[str, Any] | None = None,
    class_name: str = "identifiedDynamics",
    parameter_vector: InertialParameterVector | None = None,
    gravity_config: GravityConfig | str | np.ndarray | list[float] | None = None,
    payload_model=None,
    payload_mode: str | PayloadMode = PayloadMode.NONE,
    payload_frame_transform: np.ndarray | None = None,
) -> dict[str, Path]:
    """
    Export identified inverse-dynamics code into `output_dir`.

    Parameters
    ----------
    robot_model
        The loaded robot model whose Pinocchio model carries the chosen gravity.
    result : dict
        Identification result containing `theta_hat_full`.
    urdf_path : Path
        Kept for API clarity and metadata symmetry with the caller.
    output_dir : Path
        Destination directory.
    result_stem : str
        Usually `real` or `synthetic`.
    mlp_compensator
        当前阶段保留该参数仅为了 API 兼容；导出链默认忽略残差补偿，只生成纯物理模型。
    generation_metadata
        Metadata written into the generated files as comments. This is where
        timestamp/source/mode information belongs, while filenames stay stable.
    class_name : str
        Stable RTOSAlgorithm-side class name to generate.

    Returns
    -------
    dict[str, Path]
        Paths for the generated C source, C header, and C++ wrapper.
    """
    del urdf_path  # Pinocchio model is already built and gravity-configured by the caller.

    if pin is None:
        raise RuntimeError("Pinocchio is not available; cannot generate inverse-dynamics code.")

    try:
        import casadi as ca
        import pinocchio.casadi as cpin
    except ImportError as exc:
        raise RuntimeError(
            "CasADi and pinocchio.casadi are required. Run this pipeline in the "
            "`pinocchio_py` conda environment."
        ) from exc

    output_dir.mkdir(parents=True, exist_ok=True)
    temp_dir = output_dir / ".codegen_tmp"
    temp_dir.mkdir(parents=True, exist_ok=True)

    num_joints = int(robot_model.num_joints)
    effective_parameter_vector = (
        parameter_vector.copy()
        if parameter_vector is not None
        else InertialParameterVector.from_identification_result(result, num_joints)
    )
    effective_payload_mode = PayloadMode.from_value(
        payload_mode.value if isinstance(payload_mode, PayloadMode) else payload_mode
    )
    if payload_model is not None and effective_payload_mode == PayloadMode.LUMPED_LAST_LINK:
        effective_parameter_vector.add_to_link(
            num_joints - 1,
            payload_model.to_inertial_parameter_delta(frame_transform=payload_frame_transform),
        )
    elif payload_model is not None and effective_payload_mode == PayloadMode.EXTERNAL_WRENCH:
        raise NotImplementedError(
            "C++ export currently supports payload_mode='lumped_last_link' only. "
            "Use the Python runtime model for external-wrench payload compensation."
        )

    theta_full = np.asarray(effective_parameter_vector.to_theta_full(with_friction=True), dtype=float)
    expected_size = 12 * num_joints
    if theta_full.shape != (expected_size,):
        raise ValueError(f"Expected theta_hat_full length {expected_size}, got {theta_full.shape[0]}.")

    num_rigid_params = 10 * num_joints
    theta_rigid = ca.DM(theta_full[:num_rigid_params])
    viscous = theta_full[num_rigid_params:num_rigid_params + num_joints]
    coulomb = theta_full[num_rigid_params + num_joints:]

    cmodel = cpin.Model(robot_model.pinocchio_model)
    cdata = cmodel.createData()

    q = ca.SX.sym("q", num_joints)
    dq = ca.SX.sym("dq", num_joints)
    ddq = ca.SX.sym("ddq", num_joints)

    gravity = ca.SX.sym("gravity", 3)
    gx, gy, gz = gravity[0], gravity[1], gravity[2]

    zero_dq = ca.SX.zeros(num_joints, 1)
    zero_ddq = ca.SX.zeros(num_joints, 1)
    zero_gravity = ca.SX.zeros(3, 1)
    gravity_x = ca.vertcat(ca.SX(1.0), ca.SX(0.0), ca.SX(0.0))
    gravity_y = ca.vertcat(ca.SX(0.0), ca.SX(1.0), ca.SX(0.0))
    gravity_z = ca.vertcat(ca.SX(0.0), ca.SX(0.0), ca.SX(1.0))

    cmodel.gravity.linear = zero_gravity
    y_m = cpin.computeJointTorqueRegressor(cmodel, cdata, q, zero_dq, ddq)
    y_c = cpin.computeJointTorqueRegressor(cmodel, cdata, q, dq, zero_ddq)

    cmodel.gravity.linear = gravity_x
    y_gx = cpin.computeJointTorqueRegressor(cmodel, cdata, q, zero_dq, zero_ddq)
    cmodel.gravity.linear = gravity_y
    y_gy = cpin.computeJointTorqueRegressor(cmodel, cdata, q, zero_dq, zero_ddq)
    cmodel.gravity.linear = gravity_z
    y_gz = cpin.computeJointTorqueRegressor(cmodel, cdata, q, zero_dq, zero_ddq)

    rigid_regressor = y_m + y_c + gx * y_gx + gy * y_gy + gz * y_gz
    gravity_regressor = gx * y_gx + gy * y_gy + gz * y_gz
    zero_gravity_rigid_regressor = y_m + y_c

    rigid_tau = ca.mtimes(rigid_regressor, theta_rigid)
    gravity_tau = ca.mtimes(gravity_regressor, theta_rigid)
    zero_gravity_rigid_tau = ca.mtimes(zero_gravity_rigid_regressor, theta_rigid)
    friction_tau = ca.vertcat(
        *[
            viscous[joint_idx] * dq[joint_idx] + coulomb[joint_idx] * ca.sign(dq[joint_idx])
            for joint_idx in range(num_joints)
        ]
    )
    tau = rigid_tau + friction_tau

    function_name = "identified_inverse_dynamics"
    function = ca.Function(
        function_name,
        [q, dq, ddq, gravity],
        [tau],
        ["q", "dq", "ddq", "gravity"],
        ["tau"],
    )
    rigid_function = ca.Function(
        "identified_inverse_dynamics_rigid",
        [q, dq, ddq, gravity],
        [rigid_tau],
        ["q", "dq", "ddq", "gravity"],
        ["tau"],
    )
    gravity_function = ca.Function(
        "identified_gravity_torques",
        [q, gravity],
        [gravity_tau],
        ["q", "gravity"],
        ["tau"],
    )
    zero_gravity_rigid_function = ca.Function(
        "identified_zero_gravity_rigid_torques",
        [q, dq, ddq],
        [zero_gravity_rigid_tau],
        ["q", "dq", "ddq"],
        ["tau"],
    )
    mass_function = ca.Function(
        "identified_mass_torques",
        [q, ddq],
        [ca.mtimes(y_m, theta_rigid)],
        ["q", "ddq"],
        ["tau"],
    )
    coriolis_function = ca.Function(
        "identified_coriolis_torques",
        [q, dq],
        [ca.mtimes(y_c, theta_rigid)],
        ["q", "dq"],
        ["tau"],
    )
    gravity_x_function = ca.Function(
        "identified_gravity_xaxis_torques",
        [q],
        [ca.mtimes(y_gx, theta_rigid)],
        ["q"],
        ["tau"],
    )
    gravity_y_function = ca.Function(
        "identified_gravity_yaxis_torques",
        [q],
        [ca.mtimes(y_gy, theta_rigid)],
        ["q"],
        ["tau"],
    )
    gravity_z_function = ca.Function(
        "identified_gravity_zaxis_torques",
        [q],
        [ca.mtimes(y_gz, theta_rigid)],
        ["q"],
        ["tau"],
    )

    c_filename = f"identified_inverse_dynamics_{result_stem}_core.c"
    project_header_filename = f"{class_name}.h"
    project_cpp_filename = f"{class_name}.cpp"

    # CasADi's generator validates the raw filename as a C identifier-like name,
    # so generate from inside the output directory instead of passing a path.
    with _pushd(temp_dir):
        codegen = ca.CodeGenerator(c_filename, {"with_header": True})
        codegen.add(function)
        codegen.add(rigid_function)
        codegen.add(gravity_function)
        codegen.add(zero_gravity_rigid_function)
        codegen.add(mass_function)
        codegen.add(coriolis_function)
        codegen.add(gravity_x_function)
        codegen.add(gravity_y_function)
        codegen.add(gravity_z_function)
        codegen.generate()

    generated_core_code = _make_direct_core_code((temp_dir / c_filename).read_text(encoding="utf-8"))
    for temp_file in temp_dir.iterdir():
        temp_file.unlink()
    temp_dir.rmdir()

    del mlp_compensator

    project_header_path = output_dir / project_header_filename
    project_cpp_path = output_dir / project_cpp_filename
    _clear_stale_generated_project_files(
        output_dir,
        keep_paths={project_header_path, project_cpp_path},
    )
    _write_project_header(project_header_path, class_name, num_joints, generation_metadata)
    _write_project_cpp(
        project_cpp_path,
        class_name,
        num_joints,
        np.asarray(GravityConfig.from_any(gravity_config or robot_model.gravity_vector).gravity_vector_base, dtype=float),
        generated_core_code,
        generation_metadata,
    )

    return {
        "project_header": project_header_path,
        "project_cpp": project_cpp_path,
    }
