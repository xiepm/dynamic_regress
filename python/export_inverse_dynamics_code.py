"""
Generate controller-side inverse-dynamics code from identified parameters.

This module uses `pinocchio.casadi` to build the symbolic joint-torque
regressor, substitutes the latest identified parameter vector, and asks CasADi
to emit plain C code. The generated file is intended as the bridge from the
Python identification pipeline to embedded/controller projects.
"""

